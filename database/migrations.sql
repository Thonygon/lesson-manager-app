-- Profiles launch fields. Existing app uses profiles.user_id as the app user key.
alter table profiles
    add column if not exists role text default 'teacher',
    add column if not exists current_plan text default 'free',
    add column if not exists subscription_status text default 'free',
    add column if not exists account_status text default 'active',
    add column if not exists admin_notes text;

-- If an older role check constraint only allowed teacher/student/tutor, replace it
-- with production roles needed for admin and school accounts.
do $$
declare
    constraint_name text;
begin
    select conname into constraint_name
    from pg_constraint
    where conrelid = 'profiles'::regclass
      and contype = 'c'
      and pg_get_constraintdef(oid) like '%role%teacher%student%'
    limit 1;

    if constraint_name is not null then
        execute format('alter table profiles drop constraint %I', constraint_name);
    end if;
end $$;

alter table profiles drop constraint if exists profiles_role_allowed;

alter table profiles
    add constraint profiles_role_allowed
    check (role in ('admin', 'teacher', 'student', 'school_admin', 'tutor'));

-- Launch plans are intentionally data-driven so pricing/limits can change without code deploys.
create table if not exists plans (
    id text primary key,
    name text not null,
    price integer,
    billing_interval text not null default 'month',
    features_json jsonb not null default '{}'::jsonb,
    limits_json jsonb not null default '{}'::jsonb,
    active boolean not null default true,
    created_at timestamptz not null default now(),
    updated_at timestamptz not null default now()
);

