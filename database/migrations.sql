create table if not exists user_subscriptions (
    user_id text primary key,
    plan_id text not null references plans(id),
    subscription_status text not null default 'free',
    provider_customer_id text,
    provider_subscription_id text,
    trial_end timestamptz,
    current_period_end timestamptz,
    cancel_at_period_end boolean not null default false,
    manual_override boolean not null default false,
    created_at timestamptz not null default now(),
    updated_at timestamptz not null default now()
);

create table if not exists payment_events (
    id bigint generated by default as identity primary key,
    provider text not null,
    event_type text not null,
    payload jsonb not null default '{}'::jsonb,
    processed boolean not null default false,
    created_at timestamptz not null default now()
);

create table if not exists usage_tracking (
    user_id text primary key,
    ai_generations integer not null default 0,
    pdf_exports integer not null default 0,
    students_count integer not null default 0,
    classes_count integer not null default 0,
    monthly_reset_date date not null default current_date,
    created_at timestamptz not null default now(),
    updated_at timestamptz not null default now()
);

create table if not exists admin_overrides (
    id bigint generated by default as identity primary key,
    user_id text not null,
    override_type text not null,
    reason text,
    created_by text,
    expires_at timestamptz,
    created_at timestamptz not null default now()
);

create index if not exists idx_user_subscriptions_provider_customer_id on user_subscriptions(provider_customer_id);
create index if not exists idx_payment_events_type_created_at on payment_events(event_type, created_at desc);
create index if not exists idx_admin_overrides_user_id on admin_overrides(user_id);
